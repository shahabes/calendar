<template>
	<div class="today-button-section">
		<NcButton
			:aria-label="title"
			class="button"
			:name="title"
			@click="today()">
			{{ $t('calendar', 'Today') }}
		</NcButton>
	</div>
</template>

<script>
import moment from '@nextcloud/moment'
import { NcButton } from '@nextcloud/vue'
import { mapState } from 'pinia'
import useSettingsStore from '../../../store/settings.js'
import { formatDate } from '../../../utils/dateFormatter.js'

export default {
	name: 'EmbedHeaderTodayButton',
	components: {
		NcButton,
	},

	computed: {
		...mapState(useSettingsStore, {
			locale: 'momentLocale',
		}),

		title() {
			if (this.locale?.startsWith('fa')) {
				return formatDate(moment().toDate(), 'll', this.locale)
			}

			return moment().locale(this.locale).format('ll')
		},
	},

	methods: {
		today() {
			const name = this.$route.name
			const params = { ...this.$route.params, firstDay: 'now' }

			// Don't push new route when day didn't change
			if (this.$route.params.firstDay === 'now') {
				return
			}

			this.$router.push({ name, params })
		},
	},
}
</script>
